// Simple translation function that uses the cache or returns a placeholder
export async function translateToBengali(text: string): Promise<string> {
  // Convert to lowercase for cache lookup
  const lowerText = text.toLowerCase().trim()

  // Check if we have this exact phrase in our cache
  if (translationCache[lowerText]) {
    return translationCache[lowerText]
  }

  // Check for partial matches in our cache
  for (const key in translationCache) {
    if (lowerText.includes(key)) {
      return translationCache[key]
    }
  }

  // If no match found, return a simulated translation
  // In a real app, this would call a translation API
  return `${text} (বাংলা অনুবাদ)`
}

// Get instant translation without async
export function getInstantTranslation(text: string): string | null {
  const lowerText = text.toLowerCase().trim()

  // Check if we have this exact phrase in our cache
  if (translationCache[lowerText]) {
    return translationCache[lowerText]
  }

  // Check for partial matches in our cache
  for (const key in translationCache) {
    if (lowerText.includes(key)) {
      return translationCache[key]
    }
  }

  return null
}

// Get suggestions based on input
export function getSuggestions(input: string): Array<{ english: string; bengali: string }> {
  const lowerInput = input.toLowerCase().trim()
  const suggestions: Array<{ english: string; bengali: string }> = []

  // Find matching phrases in our cache
  for (const key in translationCache) {
    if (key.includes(lowerInput) || lowerInput.includes(key)) {
      suggestions.push({
        english: key,
        bengali: translationCache[key],
      })

      // Limit to 5 suggestions
      if (suggestions.length >= 5) {
        break
      }
    }
  }

  return suggestions
}
